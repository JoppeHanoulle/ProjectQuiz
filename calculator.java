public class calculator
{
   public int optellen (int a, int b) {
       
       return a+b;
       
    }
    
    public int vermenigvuldigen (int c, int d){
        return c*d;
        
    }
    
    public int delen (int f, int e){
        
        return f/e;
        
    }
    
    public int aftrekken (int g, int h){
        
        return g-h;
        
    }
    
    public int kwadraat (int i){
        
        return i*i;
        
    }
    
    public double vierkantsewortel (double j){
        
        return Math.sqrt(j);
        
    }
    
    public int derdemacht (int k){
        
        return k*k*k;
        
    }
}
